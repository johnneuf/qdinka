CREATE TABLE  products
(
product_id number (11) primary key,
product_name varchar(25) NOT NULL,
product_price number (11)NOT NULL,
product_type varchar(25)NOT NULL,
product_condition varchar(25) NOT NULL,
product_shipping varchar(25) NOT NULL,
product_rating varchar(25) NOT NULL,
product_color varchar(25) NOT NULL,
product_other varchar(25) NOT NULL

);